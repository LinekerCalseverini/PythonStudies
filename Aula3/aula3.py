#!/usr/local/bin/python3
# Linguagem Python
# Tipagem = Dinâmica / Forte
# str -> string -> texto
# Strings são textoes que estão dentro de aspas

# Não é string
print(1234)

# Aspas duplas
print("Luiz Otávio")

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# Caracter de escape = \ (Iguais outras linguagens)
print('Luiz \"Otávio\"')
print(2, "Luiz 'Otávio'")

# r = mostra os caracteres de escape
print(r'Luiz \"Otávio\"')
